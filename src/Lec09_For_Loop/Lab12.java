package Lec09_For_Loop;

public class Lab12 {
    public static void main(String[] args) {
        for (int i = 0; i < 10 ; i++) {
            System.out.println("Priya");
        }
        // Exp and Result Table
        // | i=0 , 1 | System.out.println("Priya");
        // | i=1 , 2 | System.out.println("Priya");
        // | i=2 , 3 | System.out.println("Priya");
        // | i=3 , 4 | System.out.println("Priya");
        // | i=4 , 5 | System.out.println("Priya");
        // | i=5 , 6 | System.out.println("Priya");
        // | i=6 , 7 | System.out.println("Priya");
        // | i=7 , 8 | System.out.println("Priya");
        // | i=8 , 9 | System.out.println("Priya");
        // | i=9 , 10 | System.out.println("Priya");
        // | i=10 , False | NA


        for (int i = 1; i <= 10 ; i++) { // 1 to 10
            System.out.println("Tyagi");
        }
        // | i=1 , 1 |
        // | i=2 , 2 |
        // | i=3 , 3 |
        // | i=4 , 4 |
        // | i=5 , 5 |
        // | i=6 , 6 |
        // | i=7 , 7 |
        // | i=8 , 8 |
        // | i=9 , 9 |
        // | i=10 , 10 |

    }
}

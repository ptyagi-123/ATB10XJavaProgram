package Lec03_Operators;

public class Lab03_UnaryOperators {
    public static void main(String[] args) {
        // Unary Operator
        int a = +10;
        int a1 = -110;
        System.out.println(a);
        System.out.println(a1);

        int b = -1;
        b = b+1;
        System.out.println(b);

    }
}

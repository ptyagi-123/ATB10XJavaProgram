package Lec09_For_Loop;

public class Lab13_OddEven {
    public static void main(String[] args) {

        // Priya - TO find the even numbers from 1 to 50
        for (int i = 0; i <=50 ; i++) {
            if(i%2==0){
                System.out.println("Even number : " + i);
            }
            else{
                System.out.println("Odd Number: " +i );
            }

        }
    }
}

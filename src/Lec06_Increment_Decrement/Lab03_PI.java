package Lec06_Increment_Decrement;

public class Lab03_PI {
    public static void main(String[] args) {
        int a= 10;
        System.out.println(++a);
        System.out.println(a);


        // POST incremenet  = Print first and then increase
        int b = 10;
        System.out.println(b++);
        System.out.println(b);
    }
}
